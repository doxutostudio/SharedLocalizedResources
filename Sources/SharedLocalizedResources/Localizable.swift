public extension Bundle {
  static var selectedLanguageBundle: Bundle {
    let selectedLanguageCode = UserDefaults.standard.string(forKey: "selectedLanguageCode") ?? UserDefaults.standard.string(forKey: "bookLanguageCode") ?? "en"
    guard let path = Bundle.module.path(forResource: selectedLanguageCode, ofType: "lproj") else {
      return Bundle.module
    }
    return Bundle(path: path) ?? Bundle.module
  }
}
